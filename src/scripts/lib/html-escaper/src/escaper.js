const entityMap = {
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    '"': "&quot;",
    "'": "&#39;",
    "/": "&#x2F;",
};

// Also export "html" so that in UMD tuleap.escaper.html is defined
export const html = function (text) {
    return String(text).replace(/[&<>"'/]/g, function fromEntityMap(s) {
        return entityMap[s];
    });
};

export const escaper = { entityMap, html };
