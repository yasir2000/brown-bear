class b201612010900_remove_wrong_burndown_cached_values extends \Tuleap\ForgeUpgrade\Bucket
{
    public function description()
    {
        return 'Remove cache burndown with wrong field id';
    }

    public function preUp()
    {
        $this->db = $this->getApi('ForgeUpgrade_Bucket_Db');
    }

    public function up()
    {
        $sql = "DELETE tracker_field_computed_cache
                FROM tracker_field
                INNER JOIN tracker_field_computed_cache
                ON tracker_field.id = tracker_field_computed_cache.field_id
                WHERE formElement_type = 'burndown'";

        $res = $this->db->dbh->exec($sql);

        if ($res === false) {
            throw new \Tuleap\ForgeUpgrade\Bucket\BucketUpgradeNotCompleteException('An error occurred while removing wrong cache values');
        }
    }
}
