    }

    /**
     * Get the API
     *
     * @return void
     */
    public function preUp()
    {
        $this->db = $this->getApi('ForgeUpgrade_Bucket_Db');
    }

    /**
     * Adding the data
     *
     * @return void
     */
    public function up()
    {
         $sql = "ALTER TABLE plugin_git_remote_servers
